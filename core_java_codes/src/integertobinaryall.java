
public class integertobinaryall {

	public static void main(String[] args) {
		
		String q =Integer.toBinaryString(10);
		System.out.println("this is binary string "+q);
		
		String q1 =Integer.toOctalString(10);
		System.out.println("this is octal number string "+q1);
		
		String q2 =Integer.toHexString(10);
		System.out.println("this is hexadecimal number string "+ q2);
		
	}

}
